public class Class04ArithmeticOperators {
    public static void main(String[] args) {
        int num1 = 20, num2 = 30;
        int sum = num1 + num2;
        int subtraction = num1 - num2;
        int multi = num1 * num2;
        double division = (double) num1 / num2;
        System.out.println("The sum of " + num1 + " + " + num2 + " is: " + sum);
        System.out.println("The subtraction of " + num1 + " - " + num2 + " is: " + subtraction);
        System.out.println("The multiplication of " + num1 + " * " + num2 + " is: " + multi);
        System.out.println("The division of " + num1 + " / " + num2 + " is: " + division);
    }
}
